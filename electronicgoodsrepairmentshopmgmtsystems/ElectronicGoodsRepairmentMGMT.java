    private void delete_usage(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_usage
        int row = tblUsage.getSelectedRow();

    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a usage record!");
        return;
    }

    if (usageList.isEmpty() || row >= usageList.size()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please click 'List All' first!");
        return;
    }

    usageList.remove(row);

    javax.swing.table.DefaultTableModel model =
        (javax.swing.table.DefaultTableModel) tblUsage.getModel();
    model.removeRow(row);

    javax.swing.JOptionPane.showMessageDialog(this, "Usage record deleted successfully!");
    }//GEN-LAST:event_delete_usage

    private void edit_usage(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_usage
        int row = tblUsage.getSelectedRow();

    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a usage record!");
        return;
    }

    if (row >= usageList.size()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please click 'List All' first!");
        return;
    }

    try {
        Object[] oldData = usageList.get(row);

        int usageId = Integer.parseInt(
            javax.swing.JOptionPane.showInputDialog(this, "Usage ID:", oldData[0])
        );
        int itemId = Integer.parseInt(
            javax.swing.JOptionPane.showInputDialog(this, "Item ID:", oldData[1])
        );
        int partId = Integer.parseInt(
            javax.swing.JOptionPane.showInputDialog(this, "Part ID:", oldData[2])
        );
        int qty = Integer.parseInt(
            javax.swing.JOptionPane.showInputDialog(this, "Quantity Used:", oldData[3])
        );
        String date = javax.swing.JOptionPane.showInputDialog(this, "Usage Date:", oldData[4]);

        usageList.set(row, new Object[]{usageId, itemId, partId, qty, date});

        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) tblUsage.getModel();

        model.setValueAt(usageId, row, 0);
        model.setValueAt(itemId, row, 1);
        model.setValueAt(partId, row, 2);
        model.setValueAt(qty, row, 3);
        model.setValueAt(date, row, 4);

        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Usage information successfully updated."
        );

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Invalid input!");
    }
    }//GEN-LAST:event_edit_usage

